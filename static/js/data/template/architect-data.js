const getRandomInt = (max) => {
  return Math.floor(Math.random() * max);
};
const getRandom = (data, length = 2) => {
  const result = [];
  const arr = [];

  for (var i = 0; i < length; i++) {
    var rdValue = getRandomInt(data.length);
    while (arr.indexOf(rdValue) >= 0) {
      rdValue = getRandomInt(data.length);
    }
    arr.push(rdValue);
    result.push(data[rdValue]);
  }
  return result;
};
export const getRandomStyleArchitect = (list) =>
  getRandom(list ? list : ArchitectStyle, 1);
